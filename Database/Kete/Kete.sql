/*****************************************
* Create the Kete database
*****************************************/
DROP DATABASE IF EXISTS Kete;
CREATE DATABASE Kete;
USE Kete;  -- MySQL command

-- create the tables
CREATE TABLE Alphabet (
	AlphabetID			INT(11)			NOT NULL		AUTO_INCREMENT,
	Letter				VARCHAR(255)	NOT NULL,
	Pronunciation		VARCHAR(255)	NOT NULL,
	Link				VARCHAR(255)	NOT NULL,
	PRIMARY KEY (AlphabetID)
);
CREATE TABLE Word (
	WordID				INT(11)	 		NOT NULL	 	AUTO_INCREMENT,
	AlphabetID			INT(11)  		NOT NULL,
	Word				VARCHAR(255) 	NOT NULL,
	WordType			VARCHAR(255) 	NOT NULL,
	Meaning				VARCHAR(255) 	NOT NULL,
	PRIMARY KEY (WordID)
);

